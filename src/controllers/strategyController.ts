export const calculateSmartGridLimits = async (req: Request, res: Response) => {
  try {
    const {
      exchange,
      segment,
      symbol,
      dataSetDays = 30,
      investment,
      minimumInvestment,
    } = req.body;

    // Validate input
    if (!exchange || !segment || !symbol || !investment) {
      return sendBadRequest(
        res,
        "exchange, segment, symbol, and investment are required"
      );
    }

    // Validate segment
    if (!["SPOT", "FUTURES"].includes(segment)) {
      return sendBadRequest(res, "segment must be SPOT or FUTURES");
    }

    // Validate exchange
    const supportedExchanges = ["BINANCE", "KUCOIN", "COINDCX"];
    if (!supportedExchanges.includes(exchange.toUpperCase())) {
      return sendBadRequest(
        res,
        `Exchange ${exchange} not supported. Supported: ${supportedExchanges.join(", ")}`
      );
    }

    // Validate dataSetDays
    const validDataSets = [3, 7, 30, 180, 365];
    if (!validDataSets.includes(dataSetDays)) {
      return sendBadRequest(
        res,
        `dataSetDays must be one of: ${validDataSets.join(", ")}`
      );
    }

    // Validate investments
    if (investment <= 0) {
      return sendBadRequest(res, "investment must be positive");
    }

    // ✅ Validate minimumInvestment only if provided
    if (minimumInvestment !== undefined) {
      if (minimumInvestment <= 0) {
        return sendBadRequest(res, "minimumInvestment must be positive");
      }

      if (minimumInvestment > investment) {
        return sendBadRequest(res, "minimumInvestment cannot exceed investment");
      }
    }

    console.log("[CALCULATE_SMART_GRID_LIMITS] Request", {
      exchange,
      segment,
      symbol,
      dataSetDays,
      investment,
      minimumInvestment,
    });

    // ✅ Generate all parameters automatically
    const { generateSmartGridParams } = await import(
      "../services/strategies/indicatorCalculator.js"
    );

    const result = await generateSmartGridParams({
      exchange,
      symbol,
      dataSetDays,
      segment: segment as "SPOT" | "FUTURES",
      investment,
      minimumInvestment,
      // No user overrides - let it calculate everything
    });

    console.log("[CALCULATE_SMART_GRID_LIMITS] Success", {
      exchange,
      segment,
      symbol,
      result,
    });

    return sendSuccess(res, "Smart Grid parameters calculated successfully", {
      exchange: result.exchange,
      symbol: result.symbol,
      segment: result.segment,

      // ✅ Auto-calculated parameters
      upperLimit: result.upperLimit,
      lowerLimit: result.lowerLimit,
      levels: result.levels,
      profitPercentage: result.profitPercentage,
      minimumInvestment: result.minimumInvestment,

      // ✅ Investment breakdown
      investment: result.investment,
      perLevelInvestment: result.perLevelInvestment,

      // ✅ Validation feedback
      validation: result.validation,

      // ✅ Recommendations
      recommendations: {
        minimumInvestmentRequired: result.validation.minimumInvestmentRequired,
        maxAffordableLevels: result.validation.maxAffordableLevels,
      },

      // ✅ Indicators (for transparency)
      indicators: result.indicators,

      metadata: {
        dataSetDays: result.dataSetDays,
        calculationMethod: "Bollinger Bands + ATR + Volatility Analysis",
        autoGenerated: true,
      },

      calculatedAt: new Date().toISOString(),
    });
  } catch (error: any) {
    console.error("[CALCULATE_SMART_GRID_LIMITS] Error", {
      error: error.message,
      stack: error.stack,
    });
    return sendServerError(res, error.message);
  }
};
