export const createStrategyController = async (req: any, res: Response) => {
  const userId = req.user.userId;

  const {
    name,
    strategyType,
    assetType,
    exchange,
    segment,
    symbol,
    investmentPerRun,
    investmentCap,
    frequency,
    takeProfitPct,
    stopLossPct,
    priceStart,
    priceStop,
    time,
    hourInterval,
    daysOfWeek,
    datesOfMonth,
    executionMode,
    // Human Grid
    lowerLimit,
    upperLimit,
    entryInterval,
    bookProfitBy,
    leverage,
    direction,
    // Smart Grid - NEW FIELDS
    type, // ✅ NEUTRAL | LONG | SHORT
    levels,
    profitPercentage, // ✅ Profit per level (%)
    dataSetDays,
    gridMode,
    recalculationInterval,
    investment, // ✅ Total investment
    minimumInvestment, // ✅ Minimum investment per order
    // UTC - NEW FIELDS
    timeFrame,
    upperLimit: utcUpperLimit,
    lowerLimit: utcLowerLimit,
  } = req.body;

  const baseRequiredFields = {
    name,
    strategyType,
    exchange,
    segment,
    symbol,
    executionMode,
  };

  if (strategyType === "HUMAN_GRID") {
    const gridRequiredFields = {
      ...baseRequiredFields,
      lowerLimit,
      upperLimit,
      entryInterval,
      bookProfitBy,
      investmentPerRun,
      investmentCap,
    };

    const missingFields = Object.entries(gridRequiredFields)
      .filter(([_, value]) => value === undefined || value === null || value === "")
      .map(([key]) => key);

    if (missingFields.length > 0) {
      return sendBadRequest(
        res,
        `Missing required fields: ${missingFields.join(", ")}`
      );
    }

    if (segment === "FUTURES") {
      if (!leverage || !direction) {
        return sendBadRequest(
          res,
          "leverage and direction are required for FUTURES segment"
        );
      }
    }
  } else if (strategyType === "SMART_GRID") {
    // ✅ UPDATED: New required fields for Smart Grid
    const smartGridRequiredFields = {
      ...baseRequiredFields,
      type, // ✅ NEUTRAL | LONG | SHORT
      levels,
      profitPercentage,
      dataSetDays,
      investment, // ✅ Total investment
      minimumInvestment, // ✅ Minimum per order
    };

    const missingFields = Object.entries(smartGridRequiredFields)
      .filter(([_, value]) => value === undefined || value === null || value === "")
      .map(([key]) => key);

    if (missingFields.length > 0) {
      return sendBadRequest(
        res,
        `Missing required fields: ${missingFields.join(", ")}`
      );
    }

    // ✅ Validate type
    if (!["NEUTRAL", "LONG", "SHORT"].includes(type)) {
      return sendBadRequest(
        res,
        "type must be one of: NEUTRAL, LONG, SHORT"
      );
    }

    // ✅ Validate dataSetDays
    const validDataSets = [3, 7, 30, 180, 365];
    if (!validDataSets.includes(dataSetDays)) {
      return sendBadRequest(
        res,
        `dataSetDays must be one of: ${validDataSets.join(', ')}`
      );
    }

    // ✅ Validate levels
    if (levels < 2 || levels > 50) {
      return sendBadRequest(
        res,
        "levels must be between 2 and 50"
      );
    }

    // ✅ Validate profit percentage
    if (profitPercentage <= 0 || profitPercentage > 100) {
      return sendBadRequest(
        res,
        "profitPercentage must be between 0 and 100"
      );
    }

    // ✅ Validate investment amounts
    if (investment <= 0) {
      return sendBadRequest(res, "investment must be positive");
    }

    if (minimumInvestment <= 0) {
      return sendBadRequest(res, "minimumInvestment must be positive");
    }

    if (minimumInvestment > investment) {
      return sendBadRequest(
        res,
        "minimumInvestment cannot exceed total investment"
      );
    }

    // ✅ Calculate per-grid amount
    const perGridAmount = investment / levels;

    if (perGridAmount < minimumInvestment) {
      return sendBadRequest(
        res,
        `Investment per level (${perGridAmount.toFixed(2)}) is below minimum investment (${minimumInvestment}). Reduce levels or increase investment.`
      );
    }

    if (segment === "FUTURES") {
      if (!leverage || !direction) {
        return sendBadRequest(
          res,
          "leverage and direction are required for FUTURES segment"
        );
      }
    }
  } else if (strategyType === "UTC") {
    // ✅ UTC Strategy Validation
    const utcRequiredFields = {
      ...baseRequiredFields,
      investmentPerRun,
      investmentCap,
    };

    const missingFields = Object.entries(utcRequiredFields)
      .filter(([_, value]) => value === undefined || value === null || value === "")
      .map(([key]) => key);

    if (missingFields.length > 0) {
      return sendBadRequest(
        res,
        `Missing required fields: ${missingFields.join(", ")}`
      );
    }

    // ✅ Validate UTC parameters
    if (!timeFrame) {
      return sendBadRequest(res, "timeFrame is required for UTC strategy");
    }

    if (utcUpperLimit === undefined || utcLowerLimit === undefined) {
      return sendBadRequest(res, "upperLimit and lowerLimit are required for UTC strategy");
    }

    if (utcUpperLimit <= utcLowerLimit) {
      return sendBadRequest(res, "upperLimit must be greater than lowerLimit");
    }

    if (segment === "FUTURES" && !leverage) {
      return sendBadRequest(res, "leverage is required for FUTURES segment");
    }
  } else {
    // Growth DCA validation
    const requiredFields = {
      ...baseRequiredFields,
      frequency,
      investmentPerRun,
      investmentCap,
    };

    const missingFields = Object.entries(requiredFields)
      .filter(([_, value]) => value === undefined || value === null || value === "")
      .map(([key]) => key);

    if (missingFields.length > 0) {
      return sendBadRequest(
        res,
        `Missing required fields: ${missingFields.join(", ")}`
      );
    }
  }

  try {
    const strategy = await createStrategy({
      userId,
      name,
      strategyType,
      assetType,
      exchange,
      segment,
      symbol,
      investmentPerRun,
      investmentCap,
      frequency,
      takeProfitPct,
      stopLossPct,
      priceStart,
      priceStop,
      time,
      hourInterval,
      daysOfWeek,
      datesOfMonth,
      executionMode,
      // Human Grid
      lowerLimit,
      upperLimit,
      entryInterval,
      bookProfitBy,
      leverage,
      direction,
      // Smart Grid
      type,
      levels,
      profitPercentage,
      dataSetDays,
      gridMode,
      recalculationInterval,
      investment,
      minimumInvestment,
      // UTC
      timeFrame,
      utcUpperLimit,
      utcLowerLimit,
    });

    if (
      strategy.executionMode === "LIVE" ||
      strategy.executionMode === "PUBLISHED"
    ) {
      await registerStrategy(strategy.id);

      // Only schedule TIME-BASED strategies
      if (strategy.type !== "HUMAN_GRID" && strategy.type !== "SMART_GRID" && strategy.nextRunAt) {
        const lambdaArn = process.env.RUN_STRATEGY_LAMBDA_ARN!;
        await scheduleStrategy({ strategy, lambdaArn });
      } else {
        console.log("[STRATEGY_SIGNAL_BASED] No AWS schedule needed", {
          strategyId: strategy.id,
          type: strategy.type,
        });
      }
    }

    if (strategy.executionMode === "BACKTEST") {
      console.log("[BACKTEST] Enqueue job for strategy:", strategy.id);
    }

    return sendSuccess(res, "Strategy created", strategy);
  } catch (error: any) {
    console.error("[CREATE_STRATEGY_ERROR]", error);
    return sendServerError(res, error.message);
  }
};

